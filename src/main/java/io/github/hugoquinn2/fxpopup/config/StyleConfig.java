package io.github.hugoquinn2.fxpopup.config;

/**
 * Configuration class for the CSS styles used in the FxPopup library.
 * This class contains constants for various CSS class names used to style different elements.
 */
public class StyleConfig {

    // =============================
    // CSS Class Constants for Form Elements
    // =============================

    /**
     * CSS class name for indicating a required field.
     */
    public static final String REQUIRED = "required";

    /**
     * CSS class name for indicating a focused field.
     */
    public static final String FOCUSED = "focused";

    /**
     * CSS class name for the icon associated with the form field.
     */
    public static final String ICON = "icon";

    /**
     * CSS class name for the form field itself.
     */
    public static final String FIELD = "field";


    // ==============================
    // CSS Class Constants for Text and Labels
    // ==============================

    /**
     * CSS class name for indicating a required text input field.
     */
    public static final String TEXT_REQUIRED = "text-required";

    /**
     * CSS class name for the context (description) area of the form.
     */
    public static final String CONTEXT = "context";

    /**
     * CSS class name for the label of a form field.
     */
    public static final String LABEL = "label";
}
